package com.class03;

public class CompoundOperator {

	public static void main(String[] args) {
		

		int num1 = 100;
		num1+=400;
		
		int num2 = 97;
		num2 -= 30;
		
		int cakePiece = 11;
		cakePiece/=4;
		
		int cake = 25;
		cake%=7;
		
		
	
		System.out.println(num1);
		System.out.println(num2);
		System.out.println(cakePiece);
		System.out.println(cake);
		
		
	}
}